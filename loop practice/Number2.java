//Print numbers from 10 to 1.
public class Number2 {
 public static void main (String args[]) {
	//for loop
	for(int i=10;i>=1;i--) {
		System.out.println(i);
	}
	//while loop
	int i=10;
	while(i<=1) {
		System.out.println(i);
		i--;
	}
	// do-while loop
	int j=10;
	do {
		System.out.println(j);
		j--;
	}while(j<=1);
}
}

