public class SquareStar1 {

	public static void main(String[] args) {
		for (int i=1;i<=5;i++) {//rows
			for( int j=1;j<=5;j++) {//column
			System.out.print("* ");	//print star in one line
			}
			System.out.println(" ");//changes line
		}
	}

}
